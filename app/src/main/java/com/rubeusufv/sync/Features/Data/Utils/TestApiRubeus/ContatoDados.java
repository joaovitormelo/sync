package com.rubeusufv.sync.Features.Data.Utils.TestApiRubeus;

public class ContatoDados {
    private String id;
    private String contatoPrincipal;
    private String[] contatosSecundarios;
    private String nome;
    private String nomeSocial;
    private String codigo;
    private String imagem;
    private String cpf;
    private String datanascimento;
    private String endereco;
    private String cep;
    private String numero;
    private String bairro;
    private String naturalidade;
    private String nacionalidade;

    // Getters e Setters
    public String getId() { return id; }
    public void setId(String id) { this.id = id; }

    public String getContatoPrincipal() { return contatoPrincipal; }
    public void setContatoPrincipal(String contatoPrincipal) { this.contatoPrincipal = contatoPrincipal; }

    public String[] getContatosSecundarios() { return contatosSecundarios; }
    public void setContatosSecundarios(String[] contatosSecundarios) { this.contatosSecundarios = contatosSecundarios; }

    public String getNome() { return nome; }
    public void setNome(String nome) { this.nome = nome; }

    public String getNomeSocial() { return nomeSocial; }
    public void setNomeSocial(String nomeSocial) { this.nomeSocial = nomeSocial; }

    public String getCodigo() { return codigo; }
    public void setCodigo(String codigo) { this.codigo = codigo; }

    public String getImagem() { return imagem; }
    public void setImagem(String imagem) { this.imagem = imagem; }

    public String getCpf() { return cpf; }
    public void setCpf(String cpf) { this.cpf = cpf; }

    public String getDatanascimento() { return datanascimento; }
    public void setDatanascimento(String datanascimento) { this.datanascimento = datanascimento; }

    public String getEndereco() { return endereco; }
    public void setEndereco(String endereco) { this.endereco = endereco; }

    public String getCep() { return cep; }
    public void setCep(String cep) { this.cep = cep; }

    public String getNumero() { return numero; }
    public void setNumero(String numero) { this.numero = numero; }

    public String getBairro() { return bairro; }
    public void setBairro(String bairro) { this.bairro = bairro; }

    public String getNaturalidade() { return naturalidade; }
    public void setNaturalidade(String naturalidade) { this.naturalidade = naturalidade; }

    public String getNacionalidade() { return nacionalidade; }
    public void setNacionalidade(String nacionalidade) { this.nacionalidade = nacionalidade; }
}
